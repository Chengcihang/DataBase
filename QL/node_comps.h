bool nequal(void * value1, void * value2, AttrType attrtype, int attrLength){
    switch(attrtype){
        case FLOAT: return (*(float *)value1 == *(float*)value2);
        case INT: return (*(int *)value1 == *(int *)value2) ;
        default:
            return (strncmp((char *) value1, (char *) value2, attrLength) == 0);
    }
}

